void solve(){
	int n; cin >> n;
	string s; cin >> s;
	ll cnt = 0;
	for(int i = 0; i < SZ(s); ++i){
		if(s[i] == '1') cnt++;
	}
	cout << cnt * (SZ(s) - 1) + SZ(s) - cnt << endl;
}


